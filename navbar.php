        document.addEventListener("DOMContentLoaded", function() {
            // Mobile Dropdown Toggle Logic
            if (window.innerWidth < 992) {
                // Handle sub-menus click in mobile
                document.querySelectorAll('.dropdown-submenu > a').forEach(function(element) {
                    element.addEventListener('click', function(e) {
                        e.preventDefault(); // Stop redirection on first click in mobile to show sub-menu
                        e.stopPropagation();

                        let nextEl = this.nextElementSibling;
                        if (nextEl && nextEl.classList.contains('dropdown-menu')) {
                            if (nextEl.style.display == 'block') {
                                nextEl.style.display = 'none';
                            } else {
                                // Close other open submenus
                                this.parentElement.parentElement.querySelectorAll('.dropdown-menu').forEach(m => m.style.display = 'none');
                                nextEl.style.display = 'block';
                            }
                        }
                    });
                });
            }
        });
    </script>
